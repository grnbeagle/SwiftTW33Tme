import UIKit

extension UIColor {
    class func tweetmeGrayColor() -> UIColor {
        return UIColor(red: 117/255, green: 135/255, blue: 149/255, alpha: 1)
    }

    class func tweetmeBabyBlueColor() -> UIColor {
        return UIColor(red: 85/255, green: 172/255, blue: 238/255, alpha: 1)
    }

    class func tweetmeDarkGrayColor() -> UIColor {
        return UIColor(red: 41/255, green: 47/255, blue: 51/255, alpha: 1)
    }
}
